import { BASE_URL, clientServer } from '@/config';
import { useSearchParams } from 'next/navigation'
import React, { useEffect, useState } from 'react'
import Dashboard from '../dashboard';
import DashboardLayout from '@/layout/DashboardLayout';
import UserLayout from '@/layout/UserLayout';
import styles from './index.module.css'
import { useRouter } from 'next/router';
import { useDispatch, useSelector } from 'react-redux';
import { getAllPosts } from '@/config/redux/action/postAction';
import {getConnectionRequest,getMyConnectionRequests,sendConnectionRequest} from '@/config/redux/action/authAction';

export default function ViewProfilePage({userProfile}) {

    const searchParamers= useSearchParams();
    const router=useRouter();
    const postReducer=useSelector((state)=>state.postReducer);
    const dispatch=useDispatch();

    const authState=useSelector((state)=>state.auth);

    const [userPosts,setUserPosts]=useState([]);
    const [isCurrentUserInConnection,setIsCurrentUserInConnection]=useState(false);
    const [isConnectionNull,setIsConnectionNull]=useState(true);

    const getUserPost= async()=>{
     await dispatch(getAllPosts());
     await dispatch(getConnectionRequest({
      token: localStorage.getItem("token")
    }));
    await dispatch(getMyConnectionRequests({ token: localStorage.getItem("token")}))
    
    
    }
    useEffect(()=>{
      let post =postReducer.posts.filter((post)=>{
        return post.userId.username===router.query.username
      })
      setUserPosts(post);
    },[postReducer.posts])
  

    useEffect(() => {
      if (!userProfile?.userId?._id) return;
    
      const profileUserId = String(userProfile.userId._id);
    
      setIsCurrentUserInConnection(false);
      setIsConnectionNull(true);


      const sent = authState.connection.find(
        c => String(c.connectionId?._id || c.connectionId) === profileUserId
      );
    
 
      const received = authState.connectionRequest.find(
        c => String(c.userId?._id) === profileUserId
      );
    
      const connection = sent || received;
      if (!connection) return;
    
      setIsCurrentUserInConnection(true);
      setIsConnectionNull(connection.status_accepted !== true);
    
    }, [authState.connection, authState.connectionRequest, userProfile?.userId?._id]);
    
    
    useEffect(() => {
      console.log("SENT (connectionRequest):", authState.connectionRequest);
      console.log("RECEIVED (connection):", authState.connection);
    }, [authState.connection, authState.connectionRequest]);
    
    
    
    
    
    useEffect(() => {
      dispatch(getConnectionRequest({
        token: localStorage.getItem("token")
      }));
    }, []);
    

    useEffect(() => {
      getUserPost();
    }, []);
    
  return (
 <UserLayout>
      <DashboardLayout>
     <div className={styles.container}>
    <div className={styles.backDropContainer}>
     
             <img className={styles.backDrop} alt="backdrop/" src={`${BASE_URL}/${userProfile.userId.profilePicture}`}  />
    </div>
    <div className={styles.profileContainer_details}>
      <div className={styles.profileContainer_flex}>

            <div style={{flex:"0.8"}}>

            <div style={{display:"flex",width:"fit-content",alignItems:"center",gap:"1.2rem"}}>

              <h2>{userProfile.userId.name}</h2>
              <p style={{color:"grey"}}>@{userProfile.userId.username}</p>

            </div>
        <div style={{display:"flex",alignItems:"center",gap:"1.2rem"}}>
        {isCurrentUserInConnection ? (
  <button className={styles.connectedButton}>
    {isConnectionNull ? "Pending.." : "Connected"}
  </button>
) : (
  <button
    onClick={() => {
      dispatch(sendConnectionRequest({
        token: localStorage.getItem("token"),
        user_id: userProfile.userId._id
      }));
    }}
    className={styles.connectBtn}
  >
    Connect
  </button>
)}

            <div 
            onClick={async()=>{
              const response = await clientServer.get(`/user/download_resume?id=${userProfile.userId._id}`);
              window.open(`${BASE_URL}/${response.data.message}`,"_blank")
            }}
            style={{cursor:"ponter"}}>
            <svg style={{width:"1.2em"}} xmlns="http://www.w3.org/2000/svg" fill="none" viewBox="0 0 24 24" strokeWidth={1.5} stroke="currentColor" className="size-6">
  <path strokeLinecap="round" strokeLinejoin="round" d="M3 16.5v2.25A2.25 2.25 0 0 0 5.25 21h13.5A2.25 2.25 0 0 0 21 18.75V16.5M16.5 12 12 16.5m0 0L7.5 12m4.5 4.5V3" />
</svg>

            </div>
            </div>

            
            <div>
            <p>{userProfile.bio}</p>
            </div>
            </div>

            <div style={{flex:"0.2"}}>
            <h3>Recent Activity</h3>
            {
              userPosts.map((post)=>{
                return (
                  <div key={post._id} className={styles.postCard}>

                    <div className={styles.card}>

                      <div className={styles.card_profileContainer}>

                        {post.media!==""?<img src={`${BASE_URL}/${post.media}`} alt='' />:
                        <div style={{width:"3.4rem",height:"3.4rem"}}></div>
                          }

                      </div>
                      <p>{post.body}</p>
                    </div>
                  </div>
                )
              })
            }
            </div>
      </div>

    </div>

    <div className={styles.workHistory}>
      <h4>Work History</h4>

      <div className={styles.workHistoryContainer}>
            {
              userProfile.pastWork.map((work,index)=>{
                return(
                  <div key={index} className={styles.workHistoryCard}>
                    <p 
                    style={{fontWeight:"bold",display:"flex",alignItems:"center",gap:"0.8rem"}}>
                      {work.company}-{work.position}</p>
                    <p>{work.years}</p>
                  </div>
                )
              })
            }
      </div>
    </div>

     </div>
      </DashboardLayout>
      </UserLayout>
  
  )
}

